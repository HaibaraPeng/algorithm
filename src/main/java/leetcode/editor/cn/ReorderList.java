package leetcode.editor.cn;

public class ReorderList {
    public static void main(String[] args) {
        Solution solution = new ReorderList().new Solution();
        ListNode node = new ListNode(1);
        node.next = new ListNode(2);
        node.next.next = new ListNode(3);
        node.next.next.next = new ListNode(4);
        node.next.next.next.next = new ListNode(5);
        solution.reorderList(node);
    }

    public static class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }

    //leetcode submit region begin(Prohibit modification and deletion)

    /**
     * Definition for singly-linked list.
     * public class ListNode {
     * int val;
     * ListNode next;
     * ListNode() {}
     * ListNode(int val) { this.val = val; }
     * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
     * }
     */
    class Solution {
        public void reorderList(ListNode head) {
            ListNode slow = head, fast = head;
            while (fast.next != null && fast.next.next != null) {
                slow = slow.next;
                fast = fast.next.next;
            }
            ListNode right = slow.next;
            ListNode reverseRight = reverse(right);
            slow.next = null;
            ListNode node = head;
            while (reverseRight != null) {
                ListNode next = node.next;
                ListNode reverseRightNext = reverseRight.next;
                node.next = reverseRight;
                reverseRight.next = next;
                node = next;
                reverseRight = reverseRightNext;
            }
        }

        private ListNode reverse(ListNode node) {
            if (node == null || node.next == null) {
                return node;
            }
            ListNode last = reverse(node.next);
            node.next.next = node;
            node.next = null;
            return last;
        }
    }
//leetcode submit region end(Prohibit modification and deletion)

}